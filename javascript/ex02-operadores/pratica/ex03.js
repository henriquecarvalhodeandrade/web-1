let num = 10;
console.log("Valor inicial de num:", num); // Saída: Valor inicial de num: 10

// Atribuição simples (=)
let novoNum = num;
console.log("novoNum após atribuição simples:", novoNum); // Saída: novoNum após atribuição simples: 10

// Atribuição de adição (+=)
num += 5; // Equivalente a: num = num + 5;
console.log("num após += 5:", num); // Saída: num após += 5: 15

// Atribuição de subtração (-=)
num -= 3; // Equivalente a: num = num - 3;
console.log("num após -= 3:", num); // Saída: num após -= 3: 12

// Atribuição de multiplicação (*=)
num *= 2; // Equivalente a: num = num * 2;
console.log("num após *= 2:", num); // Saída: num após *= 2: 24

// Atribuição de divisão (/=)
num /= 4; // Equivalente a: num = num / 4;
console.log("num após /= 4:", num); // Saída: num após /= 4: 6

// Atribuição de módulo (%=)
num %= 4; // Equivalente a: num = num % 4; (6 % 4 = 2)
console.log("num após %= 4:", num); // Saída: num após %= 4: 2