let a = 10;
let b = 5;

// Adição (+)
let soma = a + b;
console.log("Soma (a + b):", soma); // Saída: Soma (a + b): 15

// Subtração (-)
let diferenca = a - b;
console.log("Diferença (a - b):", diferenca); // Saída: Diferença (a - b): 5

// Multiplicação (*)
let produto = a * b;
console.log("Produto (a * b):", produto); // Saída: Produto (a * b): 50

// Divisão (/)
let quociente = a / b;
console.log("Quociente (a / b):", quociente); // Saída: Quociente (a / b): 2

// Módulo (%) - Resto da divisão
let resto = a % 3; // 10 dividido por 3 é 3 com resto 1
console.log("Resto (a % 3):", resto); // Saída: Resto (a % 3): 1

// Incremento (++) - aumenta o valor em 1
let c = 7;
c++; // c agora é 8
console.log("Incremento (c++):", c); // Saída: Incremento (c++): 8

// Decremento (--) - diminui o valor em 1
let d = 7;
d--; // d agora é 6
console.log("Decremento (d--):", d); // Saída: Decremento (d--): 6

// Exponenciação (**) - ES2016
let potencia = a ** 2; // 10 elevado a 2
console.log("Potência (a ** 2):", potencia); // Saída: Potência (a ** 2): 100

// Concatenação com strings (como no seu exemplo)
var x = 5 + 5;       // Adição numérica
var y = "5" + 5;     // Concatenação: número 5 é convertido para string "5"
var z = "Hello" + 5; // Concatenação
console.log("x:", x); // Saída: x: 10
console.log("y:", y); // Saída: y: "55"
console.log("z:", z); // Saída: z: "Hello5"